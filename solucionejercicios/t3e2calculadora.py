# Solicitar al usuario la base y la altura del rectángulo
base = float(input("Introduce la base del rectángulo: "))  # Convertimos a float para manejar decimales
altura = float(input("Introduce la altura del rectángulo: "))

# Calcular el área
area = base * altura

# Mostrar el área calculada
print("El área del rectángulo es:", area)
