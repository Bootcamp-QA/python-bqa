# Definición de la cadena
cadena = "Este es un ejemplo de cadena."  # Puedes cambiar este texto

# Operaciones de manipulación de cadenas
longitud = len(cadena)
mayusculas = cadena.upper()
minusculas = cadena.lower()
reemplazo = cadena.replace("ejemplo", "muestra")  # Cambia "ejemplo" por cualquier palabra que desees

# Imprimir resultados
print("Cadena original:", cadena)
print("Longitud de la cadena:", longitud)
print("En mayúsculas:", mayusculas)
print("En minúsculas:", minusculas)
print("Reemplazo de palabra:", reemplazo)
